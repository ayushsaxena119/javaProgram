package lab;

// Task 2 - Create a program in Java to declare, initialize and use local variables of all the primitive data types in Java.

public class LabTask2 {
    public static void main(String[] args) {
        byte byteNumberExample = 100;
        short shortNumberExample = 1001;
        int integerNumberExample = 6361;
        long longNumberExample = 998865544;
        float floatNumberExample = 0.3f;
        double doubleNumberExample = 223.32435664;
        char characterExample = 'K';
        boolean booleanExample = true;
        System.out.println("byteNumberExample = " + byteNumberExample);
        System.out.println("shortNumber = " + shortNumberExample);
        System.out.println("integerNumberExample = " + integerNumberExample);
        System.out.println("longNumberExample = " + longNumberExample);
        System.out.println("floatNumberExample = " + floatNumberExample);
        System.out.println("doubleNumberExample = " + doubleNumberExample);
        System.out.println("characterExample = " + characterExample);
        System.out.println("booleanExample = " + booleanExample);
    }
}